let a = 5
for (let i = 5; i >= 1; i--) {
    let b = ''
    for (let j = i; j >= 1; j--) {
        b += a
    }
    a--
    console.log(b);
}
let c = 2
for (let i = 2; i <= 5; i++) {
    let d = ''
    for (let j = 1; j <= i; j++) {
        d += c
    }
    c++
    console.log(d);
}





















