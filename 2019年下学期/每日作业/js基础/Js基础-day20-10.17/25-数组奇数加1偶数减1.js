//25.已知arr数组内容[1, 3, 5, 8, 10, 11, 16]，编码实现：数组中所有奇数加1，偶数-1,最后打印数组内容


//分析:
//定义数组  遍历数组元素
//判断数组元素是奇数还是偶数
//是奇数就+1  是偶数就-1


//步骤:

//定义数组
var arr = [1, 3, 5, 8, 10, 11, 16];

//遍历数组元素  
for(var i = 1 ; i < arr.length ; i++){

    //判断数组元素是奇数 还是偶数
    if(arr[i] % 2 == 0){

        //是偶数 -1
        arr[i] -= 1;
    }else{

        //是奇数 +1
        arr[i] += 1;
    }
}

//打印数组元素
console.log(arr);
