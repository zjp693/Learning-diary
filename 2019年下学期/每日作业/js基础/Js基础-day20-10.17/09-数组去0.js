//分析:

//遍历数组  判断数组元素是否等于0
//如果不等于0  就将这个元素存入新数组中



//步骤:
//定义原数组
var oldArr = [1,3,4,5,0,0,6,6,0,5,4,7,6,7,0,5]; 

//创建新数组
var newArr = [];

//遍历数组元素
for(var i = 0 ; i < oldArr.length ; i++){
    if(oldArr[i] != 0){
        newArr[newArr.length] = oldArr[i];
    }
}


//打印数组
console.log(newArr);
