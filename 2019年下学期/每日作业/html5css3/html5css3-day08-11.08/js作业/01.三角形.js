//1、利用JavaScript完成输出如下图形（由“空格”和“*”这两种符号组成）

//步骤:
//定义外循环  遍历等腰三角形行数
for (var i = 0; i <= 5; i++) {
    //创建字符串  用来显示等腰三角形
    var str = "";
    //创建内层一循环  用来为三角形外围增加空行
    for (var j = 0; j <= 5 - i; j++) {
        str += " ";
    }
    //创建内层二循环  用来显示星形
    for (var k = 0; k < i; k++) {
        str += "* ";
    }
    //打印字符串
    console.log(str);

}